/** Quick lookup for guess validation (Set built once). */
const WORD_SET = new Set(FILIPINO_WORDS)

export function isAllowedGuess(word: string): boolean {
  return WORD_SET.has(word.toUpperCase())
}

export function normalizeWord(raw: string): string {
  return raw.normalize('NFC').toUpperCase().replace(/[^A-Z]/g, '')
}
